package ch02;

public class CharLiteral {
	public static void main(String[] args) {
		System.out.println("\" Hello!! \"");     //"Hello" 로 출력됨
		System.out.println(" \\ Hello!! \\ ");    // \\ 묶여 출력됨 
		System.out.print(" \\ Hello!! \\ "); 
		System.out.println(" \b Hello!! \b "); 
		System.out.print(" \\ Hello!! \\ "); 
		System.out.print(" \t Hello!! \t "); 
		System.out.print(" \n Hello!! \n ");
		System.out.print(" \r Hello!! \r ");
		System.out.print(" \f Hello!! \f ");
		System.out.print("a"); 
	
	}

}
